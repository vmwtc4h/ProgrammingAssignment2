## Write a short comment describing this function
# this function creates a special "matrix" object that can cache it's inverse
library(MASS)

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y){
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinverse <- function(ginv) m <<-ginv
    getinverse <- function() m
    list(set = set, get = get, setinverse = setinverse, getinverse = getinverse)

}


## Write a short comment describing this function
# this functions computes the inverse of the special matrix that is has
# been generated by the makeCacheMatrix.  If the inverse has been calcualted
# before then the cached result will be returned

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        m <- x$getinverse()
        if (!is.null(m)){
            message("getting cached inverse value")
            return
        }
        data <- x$get()
        m<- ginv(data, ...)
        x$setinverse(m)
        m
        
        
}
